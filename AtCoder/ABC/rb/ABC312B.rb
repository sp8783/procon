n, m = gets.split.map(&:to_i)
g = n.times.map { gets.chomp }

TAK = "###.?????
###.?????
###.?????
....?????
?????????
?????....
?????.###
?????.###
?????.###"

def tak?(section)
  TAK.chars.zip(section.chars).all? { |x, y| x == "?" || x == y }
end

ans = []
(0..n - 9).each do |y|
  (0..m - 9).each do |x|
    section = g[y, 9].map { |row| row[x, 9] }.join("\n")
    ans << [y + 1, x + 1] * " " if tak?(section)
  end
end

puts ans
